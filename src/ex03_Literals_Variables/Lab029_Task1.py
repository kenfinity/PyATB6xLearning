# Take a 3 input from the user
# perform the sub, sub, mul and div

num1 = int(input("Enter a number: "))
num2 = int(input("Enter another number: "))
num3 = int(input("Enter a third number: "))

#Performing Operations
sub_result = num1 - num2 - num3 #Subtraction
print(sub_result)

add_result = num1 + num2 + num3 #Addition
print(add_result)

mul_result = num1 * num2 * num3 #Multiplication
print(mul_result)

div_result = (num1 / num2) / num3 #Division
print(div_result)