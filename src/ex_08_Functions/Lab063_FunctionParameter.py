def greet(name):
    print("Hello There", name)

greet("Pramod")
greet("Messi")
greet("Ronaldo")
greet(3.14)

def greet_first_and_last_name(firstname, lastname):
    print("Hello There", firstname, lastname)

greet_first_and_last_name("Lionel", "Messi")

# Function name is preferable to be lower case

